//luas lingkaran

//pi * radius * radius

const pi = 3.14;
let radius = prompt("Masukan Jari-jari lingkarang: ");
const luas_lingkaran = pi * radius * radius;
console.log('Jari-jari:'+ radius);
console.log('luas lingkaran:'+ luas_lingkaran);

